def main():
   n1 = int(input("nota 1: "))
   n2 = int(input("nota 2: "))
   sinal = input("digite o sinal:")
   if sinal == "+":
    return print(n1+n2)
   elif sinal == "-":
      return print(n1-n2)
   elif sinal == "*":
      return print(n1*n2)
   elif sinal == "/":
      return print(n1/n2)

main ()